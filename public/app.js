function googleLogin(){
    var provider = new firebase.auth.GoogleAuthProvider();

    firebase.auth().useDeviceLanguage();

    firebase.auth().signInWithPopup(provider)

            .then(result => {
                var user = result.user;
                document.write(`Hello ${user.displayName}`);
                console.log(user);
            })
            .catch(console.log);
}

